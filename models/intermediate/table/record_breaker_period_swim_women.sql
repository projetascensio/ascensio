SELECT 
    discipline,
    CASE 
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1956 AND 1963 THEN "1956_1963"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1964 AND 1971 THEN "1964_1971"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1972 AND 1979 THEN "1972_1979"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1980 AND 1987 THEN "1980_1987"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1988 AND 1995 THEN "1988_1995"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 1996 AND 2003 THEN "1995_2003"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 2004 AND 2011 THEN "2004_2011"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 2012 AND 2019 THEN "2012_2019"
      WHEN EXTRACT(YEAR FROM date) BETWEEN 2020 AND 2025 THEN "2020_2025"
    END as period,
    COUNT(record) as nb_record_breaken
FROM `ascencio-453311.projet_ascencio_clean.swimming_clean_women`
WHERE record = "oui"
GROUP BY discipline, period